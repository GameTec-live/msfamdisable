def checkcommand(command):
    if command == 'exit':
        print("Exiting in 3sec...")
        time.sleep(3)
        exit(0)
    elif command == 'help':
        print("""
        Crash help:
        help : show this list.
        exit : exit this CLI.
        screenfetch : show systeminfo.
        usrconfig : configuer pasword and user.
        shell : open a shell
        bash : open bash(if installed)
        start : start msfamdisable
        crypt : accsess the encrypter of the libary
        decrypt : accsess the decrypter of the libary
        show_data : show data in files
        recover : start automatic recovery
        hard_reset : hard reset the app
        """)
    elif command == 'screenfetch':
        screenfetch()
    elif command == 'usrconfig':
        libary.change()
    elif command == 'shell':
        subprocess.call([r'powershell'])
    elif command == 'bash':
        subprocess.call([r'wsl'])
    elif command == 'start':
        subprocess.call([r'start.bat'])
    elif command == 'crypt':
        data=input("Enter, what to encrypt: ")
        print(libary.crypt(data))
    elif command == 'decrypt':
        data=input("Enter, what to decrypt: ")
        print(libary.encrypt(data))
    elif command == show_data:
        print(libary.read_data())
    elif command == "recover":
        libary.recover()
    elif command == "hard_reset":
        libary.hard_reset()