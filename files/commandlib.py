def checkcommand(command):
    if command == 'exit':
        print("Exiting in 3sec...")
        time.sleep(3)
        exit(0)
    elif command == 'help':
        print("""
        Crash help:
        help : show this list.
        exit : exit this CLI.
        screenfetch : show systeminfo.
        usrconfig : configuer pasword and user.
        reset : reset software.
        restore : restore the programm
        changesetting : -1 = disable WpcTok.exe -2 = enable WpcTok.exe
        cmd : open cmd
        start : start msfamdisable
        """)
    elif command == 'screenfetch':
        screenfetch()
    elif command == 'usrconfig':
        libary.change()
    elif command == 'reset':
        input("Press enter to reset...")
        w = open("login.txt", 'w')
        w.write(str("admin")+"\n")
        w.write(str("admin")+"\n")
        w.write(str("0")+"\n")
        w.close
        w = open("data.txt", 'w')
        w.write(str("1"))
        w.close()
        os.remove("updates.txt")
    elif command == 'restore':
        w = open("data.txt", 'w')
        w.write(str("1"))
        w.close()
        x = open("updates.txt", 'w')
        x.close()
        print("restore complete")
    elif command == 'changesetting-1':
        w = open("data.txt", 'w')
        w.write(str("0"))
        w.close()
    elif command == 'chanesetting-2':
        w = open("data.txt", 'w')
        w.write(str("1"))
        w.close()
    elif command == 'cmd':
        subprocess.call([r'cmd.bat'])
    elif command == 'start':
        subprocess.call([r'start.bat'])
